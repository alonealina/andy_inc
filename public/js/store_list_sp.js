function clickStorePref1() {
    if ($('#store1').css('display') == 'flex') {
        // 表示されている場合の処理
        $('#store1').css( 'display', 'none' );
        $('#up1').css( 'display', 'none' );
        $('#down1').css( 'display', 'block' );
        
    } else {
        // 非表示の場合の処理
        $('#store1').css( 'display', 'flex' );
        $('#up1').css( 'display', 'block' );
        $('#down1').css( 'display', 'none' );
    }
}

